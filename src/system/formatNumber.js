const formatNumber = (number) => {
    number = Math.round(number);
    if (!Number.isInteger(number)) {
        return 'Invalid';
    }
    const digits = number.toString().split('');
    const {length} = digits;
    for (let i = length - 3; i >= 0; i -= 3) {
        digits.splice(i, 0, HS);
    }
    return digits.join('');
};

// =====================================================================================================================
//  E X P O R T
// =====================================================================================================================
export default formatNumber;
