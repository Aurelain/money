import {VIRTUAL_KEYWORD} from '../SETTINGS.js';

// =================================================================================================================
//  D E C L A R A T I O N S
// =================================================================================================================
export const PATTERN_VIRTUAL = new RegExp('\\b' + VIRTUAL_KEYWORD + '\\b', 'i');

// =================================================================================================================
//  P U B L I C
// =================================================================================================================
/**
 *
 */
const checkVirtual = (row) => {
    return Boolean(row.product.match(PATTERN_VIRTUAL));
};

// =================================================================================================================
//  E X P O R T
// =================================================================================================================
export default checkVirtual;
