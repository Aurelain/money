const inferOwner = (name) => {
    const {length} = name;
    for (let i = 1; i < length; i++) {
        const c = name.charAt(i);
        if (c === c.toLocaleUpperCase()) {
            return name.substring(0, i);
        }
    }
    return name;
};

// =====================================================================================================================
//  E X P O R T
// =====================================================================================================================
export default inferOwner;
