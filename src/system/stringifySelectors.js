const stringifySelectors = ({from, value, to, product}) => {
    let draft = from + ' ' + value + ' ' + to + ' ' + product;
    draft = draft.replace(/\s+/g, ' ');
    draft = draft.trim();
    return draft;
};

// =====================================================================================================================
//  E X P O R T
// =====================================================================================================================
export default stringifySelectors;
