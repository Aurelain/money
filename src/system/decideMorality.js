import {BAD, GOOD} from '../SETTINGS.js';

// =====================================================================================================================
//  D E C L A R A T I O N S
// =====================================================================================================================
const MORALITY_ACCOUNT = 'Fam'; // TODO: read this from preferences

// =====================================================================================================================
//  P U B L I C
// =====================================================================================================================
/**
 *
 */
const decideMorality = (from, to) => {
    const isExit = from.startsWith(MORALITY_ACCOUNT);
    const isEntry = to.startsWith(MORALITY_ACCOUNT);
    if (isExit && isEntry) {
        return;
    }
    if (!isExit && !isEntry) {
        return;
    }
    if (isExit) {
        return BAD;
    } else {
        return GOOD;
    }
};

// =====================================================================================================================
//  E X P O R T
// =====================================================================================================================
export default decideMorality;
