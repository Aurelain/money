import {setState} from '../store.js';

// =====================================================================================================================
//  P U B L I C
// =====================================================================================================================
/**
 *
 */
const updateReport = (report) => {
    setState((state) => {
        if (report === null) {
            delete state.report;
        } else {
            state.report = report;
        }
    });
};

// =====================================================================================================================
//  E X P O R T
// =====================================================================================================================
export default updateReport;
