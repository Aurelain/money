import {ADMIN_ACCOUNT} from '../../SETTINGS.js';

// =====================================================================================================================
//  P U B L I C
// =====================================================================================================================
/**
 *
 */
const collectBirths = (history) => {
    const births = {};
    for (const row of history) {
        const {spreadsheetId, from, to} = row;
        if (from === ADMIN_ACCOUNT) {
            births[to] = spreadsheetId;
        }
    }
    return births;
};

// =====================================================================================================================
//  E X P O R T
// =====================================================================================================================
export default collectBirths;
