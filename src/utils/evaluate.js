/**
 * https://esbuild.github.io/content-types/#direct-eval
 */
const evaluate = (code) => {
    try {
        const fn = new Function(`return (${code})`);
        return fn();
    } catch (e) {
        // console.error(e);
    }
};

// =================================================================================================================
//  E X P O R T
// =================================================================================================================
export default evaluate;
