const checkParents = (child, candidateParent) => {
    let target = child;
    while (target) {
        if (target === candidateParent) {
            return true;
        }
        target = target.parentNode;
    }
    return false;
};

// =====================================================================================================================
//  E X P O R T
// =====================================================================================================================
export default checkParents;
